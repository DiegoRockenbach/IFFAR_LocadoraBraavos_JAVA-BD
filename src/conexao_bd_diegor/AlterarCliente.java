    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        String cod = jTcod.getText();
        String nome = jTnome.getText();
        String cpf = jTcpf.getText();
        String endereco = jTendereco.getText();
        
        try{
            String query = "UPDATE `diegor_2022`.`cliente` SET nome = '"+nome+"', cpf = '"+cpf+"', endereço = '"+endereco+"' WHERE cod = '"+cod+"';";
            Statement stmt = Conexao.conecta().createStatement();
            stmt.execute(query);
            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sintaxe SQL:\n"+ex.getMessage());
        }
    }//GEN-LAST:event_jBsalvarActionPerformed


